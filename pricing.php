			function addHiddenField(form, props) {
				Object.keys(props).forEach(fieldName => {
				var field = form[fieldName];
				if (!field) {
					field = document.createElement('input');
					field.type = 'hidden';
					field.name = fieldName;
					form.appendChild(field);
				}
			field.value = props[fieldName];
			});
}

			
